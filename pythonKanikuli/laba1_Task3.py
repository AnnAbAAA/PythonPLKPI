#Напишіть програму, яка отримує вік людини (ціле число, яке не перевищує 120) і виводить
#цей вік зі словом «рік», «роки» або «років». Наприклад, «21 рік», «22 роки», «25 років».
age = int(input("Введіть свій вік: "))
if 120 < age < 0:
    print("ERROR!!!!! !!!")
else:
    if age % 10 == 1:
        print(age, "рік")
    elif age % 10 >= 2 and age % 10 <= 4:
        print(age, "роки")
    else:
        print(age, "років")