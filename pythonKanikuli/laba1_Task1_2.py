# якщо введений вік більше або дорівнює 18 і повідомлення «Ви неповнолітній! »
# якщо введений вік менше 18 років
age = int(input("Введіть свій вік: "))
if age >= 18:
    print("Ви повнолітні!")
else:
    print("Ви неповнолітній!")
