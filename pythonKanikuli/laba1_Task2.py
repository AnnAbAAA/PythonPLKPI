#Визначити чверть координатної площини, якій належить точка. Координати точки ввести з клавіатури в одну строку через пробіл.
a = input("x, y: ")
x = int(a.split()[0])
y = int(a.split()[1])

if x>0 and y>0:
    print("1 чверть")
elif x<0 and y>0:
    print("2 чверть")
elif x<0 and y<0:
    print("3 чверть")
elif x>0 and y<0:
    print("4 чверть")